    private void Lista(){
        try{
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://127.0.0.1/banco","root","");
            String sql;
            sql = "SELECT * FROM produto";
            PreparedStatement banco = (PreparedStatement) con.prepareStatement(sql);
            banco.execute();
            ResultSet resultado = banco.executeQuery(sql);
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setNumRows(0);
            
            while(resultado.next()){
                model.addRow(new Object[]{
                    resultado.getString("numero"), resultado.getString("produto"),
                    resultado.getString("quantidade"), resultado.getString("valorunitario"),
                    inpSubTotal.getText()
                });
            }
            banco.close();
            con.close();
        }catch(SQLException ex){
            System.out.println("o erro foi: " + ex);
        }
    }
    
    public String CalcSubTotal(int quant, double valor){
        double res = quant * valor;
        
        return String.valueOf(res);
    }
    
